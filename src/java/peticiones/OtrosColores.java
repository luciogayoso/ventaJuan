package peticiones;


public class OtrosColores {
    private String idProducto;
    private String foto;
    private String color;
    private String CS;
    private String CM;
    private String CL;
    private String CXL;
    private String CT;

    public String getIdProducto() {
        return idProducto;
    }


    public void setIdProducto(String idProducto) {
        this.idProducto = idProducto;
    }

    public String getFoto() {
        return foto;
    }


    public void setFoto(String foto) {
        this.foto = foto;
    }


    public String getColor() {
        return color;
    }


    public void setColor(String color) {
        this.color = color;
    }

    /**
     * @return the CS
     */
    public String getCS() {
        return CS;
    }

    /**
     * @param CS the CS to set
     */
    public void setCS(String CS) {
        this.CS = CS;
    }

    /**
     * @return the CM
     */
    public String getCM() {
        return CM;
    }

    /**
     * @param CM the CM to set
     */
    public void setCM(String CM) {
        this.CM = CM;
    }

    /**
     * @return the CL
     */
    public String getCL() {
        return CL;
    }

    /**
     * @param CL the CL to set
     */
    public void setCL(String CL) {
        this.CL = CL;
    }

    /**
     * @return the CXL
     */
    public String getCXL() {
        return CXL;
    }

    /**
     * @param CXL the CXL to set
     */
    public void setCXL(String CXL) {
        this.CXL = CXL;
    }

    /**
     * @return the CT
     */
    public String getCT() {
        return CT;
    }

    /**
     * @param CT the CT to set
     */
    public void setCT(String CT) {
        this.CT = CT;
    }
}
